// String Literal Types
let color: "red" | "orange" | "blue";
color = "orange";
// color = "grey"; // Error
console.log(color);

// Numeric Literal Types
let num: 1 | 2 | 3;
num = 3;
// num = 10; // Error
console.log(num);

// Boolean Literal Types
let isTrue: true;
isTrue = true;
// isTrue = false; // Error
console.log(isTrue);

// Other Example
let password: "secretpassword" = "secretpassword";
// password = "somethingelse"; // Error
console.log(password);
